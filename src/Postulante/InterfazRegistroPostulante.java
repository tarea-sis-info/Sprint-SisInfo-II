    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        nombre = CasillaNombre.getText();
        apellido = CasillaApellido.getText();
        ci = CasillaCI.getText();
        correo = CasillaCorreo.getText();
        telefono = CasillaTelefono.getText();
        colegio = CasillaColegio.getText();
        direccion = CasillaDir.getText();
        carrera = CasillaCarrera.getText();
        fechaNac = CasillaFecha.getText();
        
    if(nombre.isEmpty()){
        JOptionPane.showMessageDialog(this, "El campo Nombre es obligatorio.");
        return;
    }
    if(apellido.isEmpty()){
        JOptionPane.showMessageDialog(this, "El campo Apellido es obligatorio.");
        return;
    }
    if(ci.isEmpty() || !ci.matches("\\d+")){
        JOptionPane.showMessageDialog(this, "El CI es obligatorio");
        return;
    }
    if(correo.isEmpty() || !correo.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")){
        JOptionPane.showMessageDialog(this, "Debe ingresar un correo electr√≥nico v√°lido.");
        return;
    }
    if(telefono.isEmpty() || !telefono.matches("\\d{7,8}")){
        JOptionPane.showMessageDialog(this, "Debe ingresar un numero de telefono");
        return;
    }
    if(colegio.isEmpty()){
        JOptionPane.showMessageDialog(this, "El campo Colegio es obligatorio.");
        return;
    }
    if(direccion.isEmpty()){
        JOptionPane.showMessageDialog(this, "El campo Direcci√≥n es obligatorio.");
        return;
    }
    if(carrera.isEmpty()){
        JOptionPane.showMessageDialog(this, "El campo Carrera es obligatorio.");
        return;
    }
    if(fechaNac.equals("  /  /    ")){
    JOptionPane.showMessageDialog(this, "Debe ingresar la fecha de nacimiento.");
    return;
}

String[] partes = fechaNac.split("/");
try {
    int dia = Integer.parseInt(partes[0]);
    int mes = Integer.parseInt(partes[1]);
    int anio = Integer.parseInt(partes[2]);

    java.util.Calendar calendario = java.util.Calendar.getInstance();
    int anioActual = calendario.get(java.util.Calendar.YEAR);

    if(mes < 1 || mes > 12){
        JOptionPane.showMessageDialog(this, "Debe ingresar un mes valido.");
        return;
    }

    int[] diasPorMes = {31,28,31,30,31,30,31,31,30,31,30,31};
    if((anio % 4 == 0 && anio % 100 != 0) || (anio % 400 == 0)){
        diasPorMes[1] = 29; 
    }
    if(dia < 1 || dia > diasPorMes[mes-1]){
        JOptionPane.showMessageDialog(this, "Debe ingresar un dia valido");
        return;
    }

    if(anio > anioActual){
        JOptionPane.showMessageDialog(this, "El a√±o de nacimiento no puede ser mayor al actual.");
        return;
    }

} catch(Exception e){
    JOptionPane.showMessageDialog(this, "Fecha de nacimiento inv√°lida.");
    return;
}
    // Guardar en base de datos
    boolean guardado = LogicaPostulante.guardarDatosEstudiante(nombre, apellido, fechaNac, telefono, colegio, direccion, carrera, ci);

    if(guardado){
        BtnImprimir.setEnabled(true);  
    }
    }//GEN-LAST:event_BtnGuardarActionPerformed

