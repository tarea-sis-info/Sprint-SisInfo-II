    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nombre = jTextField1.getText().trim();
    String montoTxt = jTextField2.getText().trim();

    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el NOMBRE del postulante.",
                "Falta dato", JOptionPane.WARNING_MESSAGE);
        jTextField1.requestFocus();
        return;
    }
    double monto;
    try {
        monto = Double.parseDouble(montoTxt);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Ingrese un MONTO válido.",
                "Dato inválido", JOptionPane.ERROR_MESSAGE);
        jTextField2.requestFocus();
        return;
    }
    if (monto <= 0) {
        JOptionPane.showMessageDialog(this, "El MONTO debe ser mayor a 0.",
                "Dato inválido", JOptionPane.WARNING_MESSAGE);
        jTextField2.requestFocus();
        return;
    }

    boolean ok = CompraValoradoAD.registrarPagoInscripcionPorNombre(nombre, monto);

    JOptionPane.showMessageDialog(this,
            ok ? "Pago registrado."
               : "No se pudo registrar el pago.",
            "Registro de pago",
            ok ? JOptionPane.INFORMATION_MESSAGE : JOptionPane.ERROR_MESSAGE);

    if (ok) {
        jTextField2.setText("");   
        
    }
    }//GEN-LAST:event_jButton1ActionPerformed

